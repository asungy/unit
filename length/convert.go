package length

func yoctometersToMeters(yoctometers float64) float64 {
	return yoctometers * 1e-24
}

func zeptometersToMeters(zeptometers float64) float64 {
	return zeptometers * 1e-21
}

func attometersToMeters(attometers float64) float64 {
	return attometers * 1e-18
}

func femtometersToMeters(femtometers float64) float64 {
	return femtometers * 1e-15
}

func feetToMeters(feet float64) float64 {
	return feet * 0.3048
}

func metersToFeet(meters float64) float64 {
	return meters * 3.280839895
}
